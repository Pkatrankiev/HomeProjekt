package Dictionary;

public class CoupleWords {
	
	public  String wordEN;
	public  String wordBG;
	
	public CoupleWords(String wordEN, String wordBG) {

		this.wordEN = wordEN;
		this.wordBG = wordBG;
	}

	public String getWordEN() {
		return wordEN;
	}

	public void setWordEN(String wordEN) {
		this.wordEN = wordEN;
	}

	public String getWordBG() {
		return wordBG;
	}

	public void setWordBG(String wordBG) {
		this.wordBG = wordBG;
	}
	
	
}
